# 数据类型转换 str() int() float()函数
print("------str()--------")

name='caocao'
age=18
print(type(name),type(age))

print(name+"今年"+str(age))
print("------int()--------")

number='3'
number2=3.4
number3=True
print(type(number),type(number2),type(number3))
print(int(number),type(int(number)))
print(int(number2),type(int(number2)))
print(int(number3),type(int(number3)))


print("------float()--------")
float1='3.4'
float2=3.4
float3=True
print(type(float1),type(float2),type(float3))
print(float(float1),type(float(float1)))
print(float(float2),type(float(float2)))
print(float(float3),type(float(float3)))

